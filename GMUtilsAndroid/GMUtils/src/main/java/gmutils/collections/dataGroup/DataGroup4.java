package gmutils.collections.dataGroup;

public class DataGroup4<T1, T2, T3, T4> {
    public final T1 value1;
    public final T2 value2;
    public final T3 value3;
    public final T4 value4;

    public DataGroup4(T1 value1, T2 value2, T3 value3, T4 value4) {
        this.value1 = value1;
        this.value2 = value2;
        this.value3 = value3;
        this.value4 = value4;
    }
}
